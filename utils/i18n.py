_current_lang = "tr"

# Callbacks registered by UI components to be notified on language change
_change_callbacks: list = []


def set_language(lang: str) -> None:
    global _current_lang
    if lang in STRINGS and lang != _current_lang:
        _current_lang = lang
        for cb in list(_change_callbacks):
            try:
                cb(lang)
            except Exception:
                pass


def get_language() -> str:
    return _current_lang


def register_change_callback(cb) -> None:
    """Register a callable(lang: str) to be called when language changes."""
    if cb not in _change_callbacks:
        _change_callbacks.append(cb)


def unregister_change_callback(cb) -> None:
    if cb in _change_callbacks:
        _change_callbacks.remove(cb)


def t(key: str, *args) -> str:
    val = STRINGS.get(_current_lang, STRINGS["tr"]).get(key, key)
    if args:
        try:
            return val.format(*args)
        except Exception:
            return val
    return val
